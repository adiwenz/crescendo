        baseMidi: baseMidi,
        noteMs: 200,
        offsets: const [0, 2, 4, 2, 0],
        tolerance: tolerance,
      );
      for (final s in segments) {
        segmentList.add(PitchSegment(
          startMs: s.startMs + offsetMs,
          endMs: s.endMs + offsetMs,
          midiNote: s.midiNote,
          toleranceCents: s.toleranceCents,
          label: s.label,
        ));
      }
    }
    return PitchHighwaySpec(segments: segmentList);
  }

