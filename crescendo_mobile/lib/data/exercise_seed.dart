  PitchHighwaySpec scaleSpec({
    required int baseMidi,
    required int noteMs,
    required double tolerance,
    List<String>? labels,
    List<int> pattern = const [0, 2, 4, 5, 7, 5, 4, 2, 0],
  }) {
    return PitchHighwaySpec(
      segments: _patternSegments(
        baseMidi: baseMidi,
        noteMs: noteMs,
        offsets: pattern,
        tolerance: tolerance,
        labels: labels,
      ),
    );
  }

  PitchHighwaySpec arpeggioSpec({
    required int baseMidi,
    required int noteMs,
    required double tolerance,
    List<String>? labels,
  }) {
    return PitchHighwaySpec(
      segments: _patternSegments(
        baseMidi: baseMidi,
        noteMs: noteMs,
        offsets: const [0, 4, 7, 12, 7, 4, 0],
        tolerance: tolerance,
        labels: labels,
      ),
    );
  }

  PitchHighwaySpec glideSpec({
    required int startMidi,
    required int endMidi,
    required int durationMs,
    required double tolerance,
    String? label,
  }) {
    return PitchHighwaySpec(
      segments: [
        PitchSegment(
          startMs: 0,
          endMs: durationMs,
          midiNote: startMidi,
          toleranceCents: tolerance,
          label: label,
          startMidi: startMidi,
          endMidi: endMidi,
        ),
      ],
    );
  }

  PitchHighwaySpec sirenSpec({
    required int startMidi,
    required int highMidi,
    required int durationMs,
    required double tolerance,
    String? label,
  }) {
    final half = (durationMs / 2).round();
    return PitchHighwaySpec(
      segments: [
        PitchSegment(
          startMs: 0,
          endMs: half,
          midiNote: startMidi,
          toleranceCents: tolerance,
          label: label,
          startMidi: startMidi,
          endMidi: highMidi,
        ),
        PitchSegment(
          startMs: half,
          endMs: durationMs,
          midiNote: highMidi,
          toleranceCents: tolerance,
          label: label,
          startMidi: highMidi,
          endMidi: startMidi,
        ),
      ],
    );
  }

